/**
 * Interface to a bloom filter {@link <a href="https://en.wikipedia.org/wiki/Bloom_filter">Bloom filters</a>}
 *
 * Efficient fixed space no false negatives, few false positives testing of set membership.
 */
public interface BloomFilter {

    public void add(long id);

    public boolean mayContain(long id);
}
